                                selectedMedia.length) {
                              setState(() {
                                _model.uploadedLocalFiles =
                                    selectedUploadedFiles;
                              });
                              showUploadMessage(context, 'Success!');
                            } else {
                              setState(() {});
                              showUploadMessage(
                                  context, 'Failed to upload data');
                              return;
                            }
                          }

                          context.pushNamed(
                            'UploadPost',
                            queryParameters: {
                              'selectedPics': serializeParam(
                                _model.uploadedLocalFiles,
                                ParamType.FFUploadedFile,
                                true,
                              ),
                            }.withoutNulls,
